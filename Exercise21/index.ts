// Define a Fruit interface
interface Fruit {
    name: string;
    color: string;
    taste: string;
  }
  
  // Create objects for different fruits
  let apple: Fruit = { name: "Apple", color: "Red", taste: "Sweet" };
  let banana: Fruit = { name: "Banana", color: "Yellow", taste: "Sweet" };
  let orange: Fruit = { name: "Orange", color: "Orange", taste: "Citrusy" };
  let mango: Fruit = { name: "Mango", color: "Yellow", taste: "Sweet" };
  
  // Print information about the fruits
  console.log("Fruit Information:");
  console.log(apple);
  console.log(banana);
  console.log(orange);
  console.log(mango);
  